# | **List**                         | **Tuple**                           |
# | -------------------------------- | ----------------------------------- |
# | Mutable (can be changed)         | Immutable (cannot be changed)       |
# | Written using `[ ]`              | Written using `( )`                 |
# | Elements can be added or removed | Elements cannot be added or removed |
# | Uses more memory                 | Uses less memory                    |
# | Slower                           | Faster                              |
# | Many built-in methods            | Limited methods                     |
# | Used when data changes           | Used when data is fixed             |
# | Less secure                      | More secure                         |
