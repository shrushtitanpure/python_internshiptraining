# EXAMPLE 1
class Bike:
    name = "R15"
    brand = "Yamaha"
    colour = "Red and Black"

    # methods
    def speed(self):
        print("It can race up to 120 km/hr")

    def average(self):
        print("Average is 10 km/l")

    def gear(self):
        print("5 gears")

# object creation
b1 = Bike()

# accessing variables
print(b1.name)
print(b1.brand)
print(b1.colour)

# calling methods
b1.speed()
b1.average()
b1.gear()

